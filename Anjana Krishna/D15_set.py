Grocery={"Milk","Bread","Eggs","Avacado"}

#add()
Grocery.add("Tomato")
print(Grocery)
 
#update()
Grocery.update(["Onion","Flour"])
print(Grocery)


#discard
Grocery.discard("Milk")
print(Grocery)


#pop
Grocery.pop()
print(Grocery)

#clear
Grocery.clear()
print(Grocery)